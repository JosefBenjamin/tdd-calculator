package tdd;

public class Calculator {



    public int add(int one, int two) {
        int sum = one + two;
        return sum;
    }

    public int subtract(int one, int two) {
        int sum = one - two;
        return sum;
    }

    public int multiply(int one, int two) {
        int sum = one * two;
        return sum;
    }

    public double divide(int one, int two) {
        int sum = one / two;
        return sum;
    }



}
